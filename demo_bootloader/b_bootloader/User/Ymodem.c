void array_inversion(flash_parm* f_parm)
{
	if(f_parm->length <= 256)
	{
		for(uint16_t i=0;i < f_parm->length;i++)
		{
			f_parm->w_data[i]=f_parm->r_data_byte[i*4]+(f_parm->r_data_byte[i*4+1]<<8)
			+(f_parm->r_data_byte[i*4+2]<<16)+(f_parm->r_data_byte[i*4+3]<<24);
		}
	}
}
